package cn.edu.neu.citybrain.dto;

public class InterLaneInfo {
	private String interId;
	private String rid;
	private String laneId;
	private String turnDirNo;
	private String turnDirNoList;
	
	public String getInterId() {
		return interId;
	}
	public void setInterId(String interId) {
		this.interId = interId;
	}
	public String getRid() {
		return rid;
	}
	public void setRid(String rid) {
		this.rid = rid;
	}
	public String getLaneId() {
		return laneId;
	}
	public void setLaneId(String laneId) {
		this.laneId = laneId;
	}
	public String getTurnDirNo() {
		return turnDirNo;
	}
	public void setTurnDirNo(String turnDirNo) {
		this.turnDirNo = turnDirNo;
	}
	public String getTurnDirNoList() {
		return turnDirNoList;
	}
	public void setTurnDirNoList(String turnDirNoList) {
		this.turnDirNoList = turnDirNoList;
	}
}
